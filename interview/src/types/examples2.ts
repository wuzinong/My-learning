type Letters = {
  A: ['█▀█ ', '█▀█ ', '▀ ▀ '];
  B: ['█▀▄ ', '█▀▄ ', '▀▀  '];
  C: ['█▀▀ ', '█ ░░', '▀▀▀ '];
  E: ['█▀▀ ', '█▀▀ ', '▀▀▀ '];
  H: ['█ █ ', '█▀█ ', '▀ ▀ '];
  I: ['█ ', '█ ', '▀ '];
  M: ['█▄░▄█ ', '█ ▀ █ ', '▀ ░░▀ '];
  N: ['█▄░█ ', '█ ▀█ ', '▀ ░▀ '];
  P: ['█▀█ ', '█▀▀ ', '▀ ░░'];
  R: ['█▀█ ', '██▀ ', '▀ ▀ '];
  S: ['█▀▀ ', '▀▀█ ', '▀▀▀ '];
  T: ['▀█▀ ', '░█ ░', '░▀ ░'];
  Y: ['█ █ ', '▀█▀ ', '░▀ ░'];
  W: ['█ ░░█ ', '█▄▀▄█ ', '▀ ░ ▀ '];
  ' ': ['░', '░', '░'];
  ':': ['#', '░', '#'];
  '*': ['░', '#', '░'];
};

type Printable = keyof Letters;
type ValidChar = Printable | '\n';

type ToAsciiArt<Text extends string, Out extends string[] = ['', '', '']> = [
  Uppercase<Text>,
  Out
] extends [
  `${infer Char extends ValidChar}${infer RestStr}`,
  [
    ...infer RestOut extends string[],
    infer L1 extends string,
    infer L2 extends string,
    infer L3 extends string
  ]
]
  ? Char extends '\n'
    ? ToAsciiArt<RestStr, [...RestOut, L1, L2, L3, '', '', '']>
    : Char extends Printable
    ? ToAsciiArt<
        RestStr,
        [
          ...RestOut,
          `${L1}${Letters[Char][0]}`,
          `${L2}${Letters[Char][1]}`,
          `${L3}${Letters[Char][2]}`
        ]
      >
    : never
  : Out;

